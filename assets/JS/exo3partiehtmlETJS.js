window.addEventListener('scroll', function () {
    var scrollValue = window.scrollY;
    if (scrollValue >= 250) {
        document.getElementById('floatGeneral').style.opacity = 1;
        document.getElementById('floatGeneral').style.transition = 'opacity 2s';
    } else {
        document.getElementById('floatGeneral').style.opacity = 0;
    }

})
